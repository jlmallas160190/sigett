package edu.unl.sigett.seguimiento.session;

import edu.unl.sigett.entity.TipoActividad;
import java.io.Serializable;
import javax.enterprise.context.SessionScoped;
import javax.inject.Named;

/**
 *
 * @author JorgeLuis
 */
@Named
@SessionScoped
public class SessionTipoActividad implements Serializable{

    private TipoActividad tipoActividad;

    public SessionTipoActividad() {
        this.tipoActividad = new TipoActividad();
    }

    public TipoActividad getTipoActividad() {
        return tipoActividad;
    }

    public void setTipoActividad(TipoActividad tipoActividad) {
        this.tipoActividad = tipoActividad;
    }

}

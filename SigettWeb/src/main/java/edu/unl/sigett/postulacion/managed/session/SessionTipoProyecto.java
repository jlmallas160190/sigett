package edu.unl.sigett.postulacion.managed.session;

import edu.unl.sigett.entity.TipoProyecto;
import java.io.Serializable;
import javax.enterprise.context.SessionScoped;
import javax.inject.Named;

/**
 *
 * @author JorgeLuis
 */

package edu.unl.sigett.postulacion.managed.session;

import edu.unl.sigett.entity.EstadoProyecto;
import java.io.Serializable;
import javax.enterprise.context.SessionScoped;
import javax.inject.Named;

/**
 *
 * @author JorgeLuis
 */
